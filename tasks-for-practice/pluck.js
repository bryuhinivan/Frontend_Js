/* Реализуйте функцию, которая принимает последовательность объектов и имя свойства и возвращает последовательность, содержащую именованное свойство каждого объекта.

Например:

pluck([{a:1}, {a:2}], 'a') // -> [1,2]
pluck([{a:1, b:3}, {a:2}], 'b') // -> [3, undefined]
Если у объекта отсутствует свойство, вы должны просто оставить его как undefined/None в выходном массиве. */

function pluck(objects, propertyName) {
  return objects.map(obj => obj[propertyName]);
}

// Примеры использования:
console.log(pluck([{a: 1}, {a: 2}], 'a')); // [1, 2]
console.log(pluck([{a: 1, b: 3}, {a: 2}], 'b')); // [3, undefined]

